let a = 9
let b = 3

//Adição
console.log("A soma será igual a:", a+b)

//Subtração
console.log("A diferença será igual a:", a-b)

//Multiplicação
console.log("O produto será igual a:", a*b)

//Divisão
console.log("O quociente será igual a:", a/b)

